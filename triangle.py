def area(a, b, c):
    return (a + b + c) / 2
''' Takes 2 numbers:
a is the first side of the triangle,
b is the second side of the triangle,
c is the third side of the triangle.
Returns the area of the triangle'''

def perimeter(a, b, c):
    return a + b + c
''' Takes 2 numbers:
a is the first side of the triangle,
b is the second side of the triangle,
c is the third side of the triangle.
Returns the perimeter of the triangle'''
